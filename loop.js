const namaMantan = ["komang", "teguh", "made"];

// For
for (let index = 0; index < namaMantan.length; index++) {
    const element = namaMantan[index];
    console.log(element);
}

// While
let index = 0;
while (index < namaMantan.length) {
    const element = namaMantan[index];
    console.log(element);
    index++
}